package main;

public class Calculadora {

	public double somar(int a, int b) {
		
		return a+b;
		
	}
	
	public double subtrair(int a, int b) {
		
		return a-b;
		
	}
	
	public double multiplicar(int a, int b) {
		
		return a * b;
		
	}
	
	public double dividir(int a, int b) {
		
		return a / b;
		
	}
	
	public boolean verificaPar(int a) {
		
		return a % 2 == 0;
		
	}

}
