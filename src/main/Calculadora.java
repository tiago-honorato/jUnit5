package main;

public class Calculadora {

	public double somar(int a, int b) {
		
		return a+b;
		
	}
	
	public double subtrair(int a, int b) {
		
		return a-b;
		
	}
	
	public boolean verifyEven(int a) {
		
		return a % 2 == 0;
		
	}
	
	public String verifyString(String a) {
		
		return a;
		
	}

}
